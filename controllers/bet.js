exports.addBet = async (req, res) => {
    try {
        let dataInput = {
          user_id: parseInt(req.body.user_id),
          matches_id: parseInt(req.body.matches_id),
          bet_team: req.body.bet_team,
          bet_money: req.body.bet_money,
        }
        
        let user = await LeUser.findOne({
            where: {
                user_id: dataInput.user_id,
            }
        })
        let match = await Matches.findOne({
            where: {
                matches_id: dataInput.matches_id
            }
        })

        if (user && match){
            try{
                if(dataInput.bet_money <= user.user_credit){
                    await Bet.create(dataInput)

                    let balance = {
                        user_credit: user.user_credit-dataInput.bet_money
                    }
                    await user.update(balance)
                    let response = {
                        status: true,
                        message: "Name: " + user.user_name + " Match: " + match.matches_id + " Team: " + dataInput.bet_team + " Money: " + dataInput.bet_money
                    }
                    res.json(response)
                }else{
                    let response = {
                        status: false,
                        message: "Not enough credit"
                    }
                    res.json(response)
                }
                
            }
            catch(err){
                let response = {
                    status: false,
                    message: "err2: " + err
                }
                res.json(response)
            }
        }else{
            let response = {
                status: false,
                message: "Don't have user or match"
            }
            res.json(response)
        }

      } catch (err) {
        let response = {
          status: false,
          message: "err1:" + err
        }
        res.json(response)
      }
}

